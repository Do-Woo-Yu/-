word CRC16(byte *buf, word size)
{
	word crc=0xFFFF;
	byte   i;	
	unsigned char *p = buf;
	
	while (size--)
	{
		i=(crc ^ (*p++));
		crc = CRC16_Table[i & 0xFF] ^ (crc >> 8);
	}
	return crc;
}

